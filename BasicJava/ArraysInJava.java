public class ArraysInJava {
      public static void main(String args[]) {
         int[] arr;  // Declaration of an Integer array

         arr= new int[3];

        // Array Initialized

         arr[0]=12;
         arr[1]=15;
         arr[2]=17;

         // Printing the elements of the arrays
         System.out.println("The element in array at index 0 = "+arr[0]);
         System.out.println("The element in array at index 1 = "+arr[1]);
         System.out.println("The element in array at index 2 = "+arr[2]);
     }
}
